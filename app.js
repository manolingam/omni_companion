const init = async() => {
    // Modern dapp browsers...
    if (window.ethereum) {
        Web3.providers.HttpProvider.prototype.sendAsync = Web3.providers.HttpProvider.prototype.send;
        window.web3 = new Web3(ethereum);
        try {
            // Request account access if needed
            await ethereum.enable();
            // Acccounts now exposed
            console.log("Success")
        } catch (error) {
            // User denied account access...
            console.log(error)
        }
    }
    // Legacy dapp browsers...
    else if (window.web3) {
        window.web3 = new Web3(web3.currentProvider);
        console.log("Connected to Metamask..")
    }
    // Non-dapp browsers...
    else {
        console.log('Non-Ethereum browser detected. You should consider trying MetaMask!');
        web3 = new Web3(new Web3.providers.HttpProvider('http://localhost:7545'));
    }

}

const balance = async() => {

    var address = document.getElementById('address').value

    var gratitudeAddress = '0xa5c9C652E9bC07352feEaFec5d4503aB7C253C0f'
    var helloAddress = '0x1Fda96405DD8Ee22631aBCf4f61282eaE802012f'
    var dunkonAddress = '0xeF7D4cf6686418c002947AD4953a3Eb325500f93'

    var gratitudeInst = web3.eth.contract(abi).at(gratitudeAddress)
    var helloInst = web3.eth.contract(abi).at(helloAddress)
    var dunkonInst = web3.eth.contract(abi).at(dunkonAddress)

    gratitudeInst.balanceOf(address, (err, result) => {
        if(!err) {
            document.getElementById('gratitude').innerText = 'Gratitude Tokens:  ' + (result.toNumber()).toString().slice(0, -18) 
        } else {
            console.log(err)
        }
    })

    helloInst.balanceOf(address, (err, result) => {
        if(!err) {
            document.getElementById('hello').innerText = 'Hello Tokens:  ' + (result.toNumber()).toString().slice(0, -18)
        } else {
            console.log(err)
        }
    })

    dunkonInst.balanceOf(address, (err, result) => {
        if(!err) {
            document.getElementById('dunkon').innerText = 'Dunkon Tokens:  ' + (result.toNumber()).toString().slice(0, -18)
        } else {
            console.log(err)
        }
    })

    document.getElementById('address').value = ''
}

